package janJavaProgramming.week9.ex4;

public class HappinessRecord {



    protected String country, rank, score;

    public String getCountry() {
        return country;
    }

    public String getRank() {
        return rank;
    }

    public String getScore() {
        return score;
    }

    public HappinessRecord(String country, String rank, String score) {
        this.country = country;
        this.rank = rank;
        this.score = score;

    }


}
